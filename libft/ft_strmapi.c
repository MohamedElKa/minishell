#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*p;

	if (!s || !f)
		return (NULL);
	i = 0;
	p = (char *)malloc(ft_strlen(s) + 1);
	if (!p)
		exit(1);
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}

#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90))
		return (1);
	return (0);
}

int	ft_isupper(char c)
{
	if ((c >= 'A' && c <= 'Z') || ft_isdigit(c) || (c == '_'))
		return (1);
	return (0);
}
